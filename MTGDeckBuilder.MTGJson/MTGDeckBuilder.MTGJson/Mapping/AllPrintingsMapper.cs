                    SetCode = deserializedSetCard.SetCode,
                    Side = deserializedSetCard.Side,
                    Signature = deserializedSetCard.Signature,
                    Subtypes = deserializedSetCard.Subtypes,
                    Supertypes = deserializedSetCard.Supertypes,
                    Text = deserializedSetCard.Text,
                    Toughness = deserializedSetCard.Toughness,
                    Type = deserializedSetCard.Type,
                    Types = deserializedSetCard.Types,
                    UUID = deserializedSetCard.UUID,
                    Variations = deserializedSetCard.Variations,
                    Watermark = deserializedSetCard.Watermark
                }).ToArray(),
                CardsphereSetId = deserializedSet.CardsphereSetId,
                Code = deserializedSet.Code,
                CodeV3 = deserializedSet.CodeV3,
                IsForeignOnly = deserializedSet.IsForeignOnly,
                IsFoilOnly = deserializedSet.IsFoilOnly,
                IsNonFoilOnly = deserializedSet.IsNonFoilOnly,
                IsOnlineOnly = deserializedSet.IsOnlineOnly,
                IsPaperOnly = deserializedSet.IsPaperOnly,
                IsPartialPreview = deserializedSet.IsPartialPreview,
                KeyruneCode = deserializedSet.KeyruneCode,
                McmId = deserializedSet.McmId,
                McmIdExtras = deserializedSet.McmIdExtras,
                McmName = deserializedSet.McmName,
                MtgoCode = deserializedSet.MtgoCode,
                Languages = deserializedSet.Languages,
                Name = deserializedSet.Name,
                ParentCode = deserializedSet.ParentCode,
                ReleaseDate = deserializedSet.ReleaseDate,
                TcgplayerGroupId = deserializedSet.TcgplayerGroupId,
                Tokens = deserializedSet.Tokens?.Select(deserializedSetToken => new ParsedAllPrintingsSetToken
                {
                    Artist = deserializedSetToken.Artist,
                    AsciiName = deserializedSetToken.AsciiName,
                    Availability = deserializedSetToken.Availability,
                    BoosterTypes = deserializedSetToken.BoosterTypes,
                    BorderColor = deserializedSetToken.BorderColor,
                    CardParts = deserializedSetToken.CardParts,
                    ColorIdentity = deserializedSetToken.ColorIdentity,
                    ColorIndicator = deserializedSetToken.ColorIndicator,
                    Colors = deserializedSetToken.Colors,
                    FaceName = deserializedSetToken.FaceName,
                    FaceFlavorName = deserializedSetToken.FaceFlavorName,
                    Finishes = deserializedSetToken.Finishes,
                    FlavorText = deserializedSetToken.FlavorText,
                    FrameEffects = deserializedSetToken.FrameEffects,
                    FrameVersion = deserializedSetToken.FrameVersion,
                    HasFoil = deserializedSetToken.HasFoil,
                    HasNonFoil = deserializedSetToken.HasNonFoil,
                    Identifiers = deserializedSetToken.Identifiers,
                    IsFullArt = deserializedSetToken.IsFullArt,
                    IsFunny = deserializedSetToken.IsFunny,
                    IsOnlineOnly = deserializedSetToken.IsOnlineOnly,
                    IsPromotionalPrinting = deserializedSetToken.IsPromotionalPrinting,
                    IsReprint = deserializedSetToken.IsReprint,
                    Keywords = deserializedSetToken.Keywords,
                    Language = deserializedSetToken.Language,
                    Layout = deserializedSetToken.Layout,
                    Loyalty = deserializedSetToken.Loyalty,
                    Name = deserializedSetToken.Name,
                    Number = deserializedSetToken.Number,
                    OtherFaceUUIDs = deserializedSetToken.OtherFaceUUIDs,
                    Power = deserializedSetToken.Power,
                    PromoTypes = deserializedSetToken.PromoTypes,
                    ReverseRelatedUUIDs = deserializedSetToken.ReverseRelatedUUIDs,
                    SecurityStamp = deserializedSetToken.SecurityStamp,
                    SetCode = deserializedSetToken.SetCode,
                    Side = deserializedSetToken.Side,
                    Signature = deserializedSetToken.Signature,
                    Subtypes = deserializedSetToken.Subtypes,
                    Supertypes = deserializedSetToken.Supertypes,
                    Text = deserializedSetToken.Text,
                    Toughness = deserializedSetToken.Toughness,
                    Type = deserializedSetToken.Type,
                    Types = deserializedSetToken.Types,
                    UUID = deserializedSetToken.UUID,
                    Watermark = deserializedSetToken.Watermark
                }).ToArray(),
                SealedProduct = deserializedSet.SealedProduct != null ? new ParsedAllPrintingsSetSealedProduct() : null,
                TotalSetSize = deserializedSet.TotalSetSize,
                Translations = deserializedSet.Translations != null ? new ParsedAllPrintingsSetTranslations
                {
                    ChineseSimplified = deserializedSet.Translations.ChineseSimplified,
                    ChineseTraditional = deserializedSet.Translations.ChineseTraditional,
                    French = deserializedSet.Translations.French,
                    German = deserializedSet.Translations.German,
                    Italian = deserializedSet.Translations.Italian,
                    Japanese = deserializedSet.Translations.Japanese,
                    Korean = deserializedSet.Translations.Korean,
                    PortugueseBrazil = deserializedSet.Translations.PortugueseBrazil,
                    Russian = deserializedSet.Translations.Russian,
                    Spanish = deserializedSet.Translations.Spanish
                } : null,
                Type = deserializedSet.Type
            };
        }
    }
