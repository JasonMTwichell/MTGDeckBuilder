                    } : null,
                    Rarity = deserializedSetCard.Rarity,
                    RebalancedPrintingUUIDs = deserializedSetCard.RebalancedPrintingUUIDs,
                    Rulings = deserializedSetCard.Rulings?.Select(deserializedSetCardRuling => new ParsedAllPrintingsRuling
